/*计算modbus的crc*/
uint16_t CRC16_Modbus(uint8_t *_pBuf, uint16_t _usLen)
{
  uint8_t ucCRCHi = 0xFF; /* 高CRC字节初始化 */
  uint8_t ucCRCLo = 0xFF; /* 低CRC 字节初始化 */
  uint16_t usIndex;       /* CRC循环中的索引 */

  while (_usLen--)
  {
    usIndex = ucCRCHi ^ *_pBuf++; /* 计算CRC */
    ucCRCHi = ucCRCLo ^ s_CRCHi[usIndex];
    ucCRCLo = s_CRCLo[usIndex];
  }
  return ((uint16_t)ucCRCHi << 8 | ucCRCLo);
}

/*modbus使用的串口初始化*/
// 选择用串口4
void modbus_hardware_init(void)
{
  /*串口GPIO初始化*/
  gpio_init_pin(GPIO_C, 10, 2, 0, 2, 1, 8); // 串口四tx
  gpio_init_pin(GPIO_C, 11, 2, 0, 2, 1, 8); // 串口四rx

  /*串口初始化*/
  __HAL_RCC_UART4_CLK_ENABLE();
  huart4.Instance = UART4;
  huart4.Init.BaudRate = 115200;
  huart4.Init.WordLength = UART_WORDLENGTH_8B;
  huart4.Init.StopBits = UART_STOPBITS_1;
  huart4.Init.Parity = UART_PARITY_NONE;
  huart4.Init.Mode = UART_MODE_TX_RX;
  huart4.Init.HwFlowCtl = UART_HWCONTROL_NONE;
  huart4.Init.OverSampling = UART_OVERSAMPLING_16;
  HAL_UART_Init(&huart4);

  __set_priority(UART4_IRQn, 3, 0);
  __enable_IRQ(UART4_IRQn);
  HAL_UART_Receive_IT(&huart4, &modbus_temp, 1);
}

/*modbus串口中断服务程序*/
void UART4_IRQHandler(void)
{
  if (UART4->ISR & 0x20) // 如果有数可读
  {
    huart4.RxISR(&huart4); // 这个是执行赋值的操作
    modbus_buffer[modbus_buffer_cnt] = modbus_temp;
    modbus_buffer_cnt++;
    hard_timer_set_point(1750); // 开始计时,1750

    UART4->RQR |= 1 << 3;                          // 清除标志位
    HAL_UART_Receive_IT(&huart4, &modbus_temp, 1); // 重启接收
  }
}

