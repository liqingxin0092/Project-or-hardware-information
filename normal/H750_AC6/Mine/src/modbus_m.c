/*主循环中modbus处理*/
void modbus_handle(void)
{
  uint16_t crc1;
  if (hard_timer_get_result()) // 如果还没接收到一帧
  {
    crc1 = CRC16_Modbus(modbus_buffer, modbus_buffer_cnt); // CRC校验
    if (crc1 != 0)
    {
      modbus_buffer_cnt = 0;
      return;
    }

    if (modbus_buffer_cnt < 4) // 数量
    {
      modbus_buffer_cnt = 0;
      return;
    }

    if (modbus_buffer[0] != 0x01) // 从机地址,设从机地址为0x01
    {
      modbus_buffer_cnt = 0;
      return;
    }
    modbus_Application_analysis(); // 应用解析

    modbus_buffer_cnt = 0;
  }
}
