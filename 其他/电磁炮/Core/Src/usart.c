float stata_transition(uint8_t High,uint8_t Low)
{
	short transition; 

	transition=((High<<8)+Low); 
	return 
		transition/100+(transition%100)*0.01;					
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
   if(huart->Instance==USART2)    //树莓派
   {
    	static uint8_t Count=0;
      static uint8_t  Usart_Receive[7];
      Usart_Receive[Count]=camera_buf;

      if(Usart_Receive[0]==0x60||Count>0)
        Count++;
      else
        Count=0;
    
      if(Count==7)
      {
        Count=0;
     
        if(Usart_Receive[6]==0xff)
        {
 
          AutoAngle = stata_transition(Usart_Receive[1],Usart_Receive[2]);
       
          AutoDisatance = stata_transition(Usart_Receive[3],Usart_Receive[4]);
       
          Target_True  =  Usart_Receive[5];
        }
      }
      
	    HAL_UART_Receive_IT(&huart2, (uint8_t *)&camera_buf, 1);
   }

	 if(huart->Instance==USART1)
	 {
		 if(0xff==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])   //复位
		 {			
		 __set_FAULTMASK(1);
				HAL_NVIC_SystemReset();
		 }
			if(0x55==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])   //手动充能
				flag=1;
			
			else if(0x56==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])  //结束充能
				flag=2;
			
			else if(0x57==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])   //发射
				flag=3;
			
			else if(0x58==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])   //自动充能
				flag=4;
			
			else if(RxBuffer[0]==0x01)           //设定时间
				Time =(RxBuffer[2]<<8)+RxBuffer[1];
			
			else if(RxBuffer[0]==0x02)           //设定距离 并且设置角度
			{
				Distance =(RxBuffer[2]<<8)+RxBuffer[1];
				flag=8;
			}
			
			else if(RxBuffer[0]==0x03)           //设定俯仰角
			{
					if(RxBuffer[2]==0xff)
					{
						angleY= ~RxBuffer[1]+1;
						AngleY= -angleY;
					}
					else
						AngleY=RxBuffer[1];
					
					flag=5;
			}
			else if(RxBuffer[0]==0x04)           //设定偏航角
			{
					if(RxBuffer[2]==0xff)
					{
						angleX= ~RxBuffer[1]+1;
						AngleX= -angleX;
					}
					else
						AngleX=RxBuffer[1];
					
					flag=7;
			}
			else if(0x59==RxBuffer[0]&&0xff==RxBuffer[1]&&0xff==RxBuffer[2])   //一键启动
			  flag=6;
			
			
				HAL_UART_Receive_IT(&huart1,RxBuffer,5);
	 }
}
